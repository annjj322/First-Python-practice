def plus(a,b):
    a=int(a)
    b=int(b)
    return(a+b)

def minus(a,b):
    a=int(a)
    b=int(b)
    return(a-b)

def mult(a,b):
    a=int(a)
    b=int(b)
    return(a*b)

def div(a,b):
    a=int(a)
    b=int(b)
    return(a/b)

print(mult("5",5))
print(plus(5,4))
print(div(9,3))
print(minus(9,"6"))